# news/pytest_tests/test_content.py
"""
Тесты содержимого страниц проекта ya_news на pytest.

Проверяют:
- Количество новостей на главной странице (пагинация).
- Сортировку новостей от новых к старым. Свежие новости - в начале списка.
- Сортировку комментариев от старых к новым.
- Наличие формы комментария для авторизованных пользователей.
- Отсутствие формы комментария для анонимных пользователей.
"""
import pytest
from django.urls import reverse
from django.conf import settings

from news.forms import CommentForm


@pytest.mark.django_db
class TestHomePage:
    """
    Тестирование главной страницы.

    Проверяет:
    - Количество новостей на странице.
    - Сортировку новостей по дате.
    """

    def test_news_count(self, client, many_news):
        """
        Назначение теста 4.

        Проверяет, что на главной странице отображается ровно
        NEWS_COUNT_ON_HOME_PAGE новостей.

        Шаги теста:
        1. Создаём 11 новостей с помощью фикстуры many_news.
        2. Выполняем GET-запрос к главной странице.
        3. Получаем список новостей из контекста ответа.
        4. Сравниваем количество новостей в списке с настройкой
           NEWS_COUNT_ON_HOME_PAGE (должно быть 10).

        Ожидаемый результат:
            На главной странице отображается ровно 10 новостей,
            несмотря на то, что в базе данных их 11.
        """
        # Получаем URL главной страницы
        url = reverse('news:home')

        # Выполняем GET-запрос
        response = client.get(url)

        # Получаем список новостей из контекста
        object_list = response.context['object_list']

        # Проверяем, что на странице 10 новостей
        assert object_list.count() == settings.NEWS_COUNT_ON_HOME_PAGE

    def test_news_order(self, client, many_news):
        """
        Назначение теста 5.

        Проверяет, что новости отсортированы от новых к старым.

        Шаги теста:
        1. Создаём новости с разными датами с помощью фикстуры many_news.
           Самая новая новость — сегодня, самая старая — 10 дней назад.
        2. Выполняем GET-запрос к главной странице.
        3. Получаем список новостей из контекста ответа.
        4. Извлекаем даты всех новостей в том порядке, в котором они
           отображаются на странице.
        5. Сортируем даты по убыванию (от новых к старым).
        6. Сравниваем исходный порядок с отсортированным.
        """
        # Получаем URL главной страницы
        url = reverse('news:home')

        # Выполняем GET-запрос
        response = client.get(url)

        # Получаем список новостей из контекста
        object_list = response.context['object_list']

        # Собираем даты новостей в список
        all_dates = [news.date for news in object_list]

        # Сортируем даты по убыванию (от новых к старым)
        sorted_dates = sorted(all_dates, reverse=True)

        # Проверяем, что порядок правильный
        assert all_dates == sorted_dates


@pytest.mark.django_db
class TestDetailPage:
    """
    Тестирование страницы отдельной новости.

    Проверяет:
    - Сортировку комментариев от старых к новым.
    - Наличие формы комментария для авторизованных пользователей.
    - Отсутствие формы комментария для анонимных пользователей.
    """

    def test_comments_order(self, client, detail_url, comments):
        """
        Назначение теста 6.

        Проверяет, что комментарии отсортированы от старых к новым.

        Шаги теста:
        1. Создаём 10 комментариев с разными датами с помощью фикстуры
        comments. Самый старый комментарий — index=0, самый новый — index=9.
        2. Выполняем GET-запрос к странице новости.
        3. Получаем новость из контекста ответа.
        4. Получаем все комментарии к этой новости.
        5. Извлекаем временные метки всех комментариев.
        6. Сортируем временные метки по возрастанию (от старых к новым).
        7. Сравниваем исходный порядок с отсортированным.
        """
        # Выполняем GET-запрос к странице новости
        response = client.get(detail_url)

        # Получаем новость из контекста
        news = response.context['news']

        # Получаем все комментарии к новости
        all_comments = news.comment_set.all()

        # Собираем временные метки комментариев
        all_timestamps = [comment.created for comment in all_comments]

        # Сортируем временные метки (по возрастанию — от старых к новым)
        sorted_timestamps = sorted(all_timestamps)

        # Проверяем, что порядок правильный
        assert all_timestamps == sorted_timestamps

    def test_anonymous_client_has_no_form(self, client, detail_url):
        """
        Назначение теста 7.

        Проверяет, что анонимный пользователь не видит форму комментария.

        Шаги теста:
        1. Выполняем GET-запрос к странице новости от анонимного клиента.
        2. Проверяем, что в контексте ответа отсутствует ключ 'form'.

        Ожидаемый результат:
            Анонимный пользователь не видит форму для отправки комментария.
            Форма доступна только авторизованным пользователям.
        """
        # Выполняем GET-запрос от анонимного пользователя
        response = client.get(detail_url)

        # Проверяем, что формы нет в контексте
        assert 'form' not in response.context

    def test_authorized_client_has_form(self, client, detail_url, author):
        """
        Назначение теста 8.

        Проверяет, что авторизованный пользователь видит форму комментария.

        Шаги теста:
        1. Авторизуем пользователя в тестовом клиенте.
        2. Выполняем GET-запрос к странице новости.
        3. Проверяем, что в контексте ответа присутствует ключ 'form'.
        4. Проверяем, что объект формы является экземпляром класса CommentForm.

        Ожидаемый результат:
            Авторизованный пользователь видит форму комментария,
            и эта форма является экземпляром CommentForm.
        """
        # Авторизуем пользователя
        client.force_login(author)

        # Выполняем GET-запрос
        response = client.get(detail_url)

        # Проверяем, что форма присутствует в контексте
        assert 'form' in response.context

        # Проверяем, что форма правильного типа
        assert isinstance(response.context['form'], CommentForm)
