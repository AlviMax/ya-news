# news/pytest_tests/test_routes.py
"""
Тесты маршрутов проекта ya_news на pytest.

Этот модуль содержит тесты для проверки доступности страниц
в зависимости от статуса пользователя (аноним, автор, читатель).
Проверяются три сценария:
1. Публичные страницы (главная и отдельной новости) доступны всем.
2. Страницы редактирования/удаления комментариев доступны только автору.
3. Анонимный пользователь перенаправляется на страницу входа.
"""
import pytest
from django.urls import reverse
from http import HTTPStatus


@pytest.mark.django_db
class TestRoutes:
    """
    Класс для тестирования доступности маршрутов (URL-адресов) проекта.

    Все тесты помечены декоратором @pytest.mark.django_db,
    который даёт доступ к тестовой базе данных.
    """

    def test_pages_availability(self, client, news):
        """
        Назначение теста 1.

        Проверяет, что публичные страницы доступны всем пользователям.
        Тестируются следующие страницы:
        - Главная страница (news:home) — доступна без авторизации
        - Страница новости (news:detail) — доступна без авторизации
        - Страница входа (users:login) — доступна без авторизации
        - Страница регистрации (users:signup) — доступна без авторизации

        Шаги теста:
        1. Создаём список URL-адресов для проверки.
        2. Для каждого URL выполняем GET-запрос от анонимного клиента.
        3. Проверяем, что каждый URL возвращает статус 200 (OK).

        Ожидаемый результат:
            Все публичные страницы доступны и возвращают статус 200.
        """
        # Кортеж кортежей: (имя маршрута, аргументы для reverse() или None)
        urls = (
            ('news:home', None),           # главная страница
            ('news:detail', (news.id,)),   # страница новости, нужен id новости
            ('users:login', None),         # страница входа
            ('users:signup', None),        # страница регистрации
        )

        # Проходим по всем URL-адресам
        for name, args in urls:
            # Строим URL по имени маршрута.
            # Если args — кортеж, передаём его; если None — вызываем без аргументов.
            url = reverse(name, args=args) if args else reverse(name)

            # Выполняем GET-запрос к странице
            response = client.get(url)

            # Проверяем, что страница доступна (статус 200 OK)
            assert response.status_code == HTTPStatus.OK

