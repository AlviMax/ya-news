    def test_redirect_for_anonymous_client(self, client, comment):
        """
        Назначение теста 3.

        Проверяет, что анонимный пользователь перенаправляется на
        страницу входа.

        Шаги теста:
        1. Сохраняем URL страницы входа.
        2. Для каждой страницы (редактирование и удаление):
           a. Строим URL с id комментария.
           b. Формируем ожидаемый URL редиректа (страница входа +
              параметр next).
           c. Выполняем GET-запрос от анонимного клиента.
           d. Проверяем, что статус ответа — 302 (редирект).
           e. Проверяем, что редирект ведёт на страницу входа с
              параметром next.

        Ожидаемый результат:
            Анонимный пользователь перенаправляется на страницу входа
            с параметром next, содержащим исходный URL.
        """
        # Сохраняем URL страницы входа
        login_url = reverse('users:login')

        # Перебираем страницы: редактирование и удаление
        for name in ('news:edit', 'news:delete'):
            # Строим URL для редактирования/удаления комментария
            url = reverse(name, args=(comment.id,))

            # Формируем ожидаемый URL редиректа, используем параметр next,
            # чтобы после входа пользователь вернулся на запрошенную страницу.
            redirect_url = f'{login_url}?next={url}'

            # Выполняем GET-запрос от анонимного клиента
            response = client.get(url)

            # Проверяем, что статус ответа — 302 (редирект)
            assert response.status_code == HTTPStatus.FOUND

            # Проверяем, что редирект ведёт на страницу входа с параметром next
            assert response.url == redirect_url
